<h1><a name ="allgemeineInfos"></a>Allgemeien Informationen</h1>
<p>
    ALFONS dient der Verbesserung des Unterrichts. Wir wollen die Kommunikation zwischen Schülern und Lehrern verbessern.
    Davon sollen beide Seiten profitieren. Schüler können Lehrern Feedback geben und somit ihre Anliegen direkt an die Richtige Stelle bringen.
    Lehrer können auf dieses Feedback in Form eines Kommentars antworten und so etwaige Fragen beantowrten oder sich einfach nur für das Feedback bedanken.
    Desweiteren ist es möglich Feedback zu bewerten. Die Durchschittsbewertung (zum Zeitpunkt an dem das Feedback abgesendet wird) wird im Feedback gespeichert und gibt dem Lehrer/der Lehrerin eine Idee wie ernst er das Feedback nehmen sollte.
    Außerdem können Lehrer Fragen stellen, um gezieltes Feedback zu bekommen. Dabei können Sie eindämmen welche Klassen diese Frage beantworten dürfen.
    
</p>


Der Lehrer/Die Lehrerin wird dein Feedback schlecht bewerten.
Deine Durchschittsbewertung (zum Zeitpunkt an dem das Feedback abgesendet wird) wird im Feedback gespeichert und gibt dem Lehrer/der Lehrerin eine Idee wie ernst er dein Feedback nehmen sollte.


<h1><a name="richtlinien"></a>Richtlinien für das Verfassen von Feedback und Kommentaren</h1>

<p>
Sei Respektvoll. Diese Plattform ist dazu da, den Unterricht zu verbessern, Beleidigungen und Drohungen werden nicht geduldet!
ALFONS benüzt ein Halbanonymes System, das heißt, dass im Falle eines Verstoßes, dein Name ausgeforscht werden kann.
Ohne dieser Maßnahme ist nötig, um gegen die wenigen vorgehen zu können, die sonst das System für alle anderen zerstören würden.
Wir geben deinen Namen nur weiter, wenn ein schwerer Verstoß vorliegt. Bei adäquater Nutzung von Alfons bleibt dein Name anonym.     
</p>
    
